    private void cargarDatos(){
        cargarDepartamentosCombo();
        
        if (this.empleado.getDepartamento() == null) {
           jComboBoxDepartamentosEmpleado.setSelectedIndex(0);
        } else{
           jComboBoxDepartamentosEmpleado.setSelectedItem(this.empleado.
                   getDepartamento().getNombre());
        }
      
        jLabelCedula.setText(this.empleado.getcodigoEmpleado());
        
        jTextFieldNombre.setText(this.empleado.getNombre());
        jTextFieldDireccion.setText(this.empleado.gettituloUniversitario());
        jTextFieldCargo.setText(this.empleado.gethorasMes());
        jTextFieldSalario.setText(String.valueOf(this.empleado.getcostoHora()));
        jComboBoxNacionalidad.setSelectedItem(this.empleado.getciudadNatal());
        jComboBoxAnioNac.setSelectedItem(String.valueOf(this.empleado.getFechaNacimiento().getYear()));
        jComboBoxMesNac.setSelectedIndex(this.empleado.getFechaNacimiento().getMonthValue() -1);
        jComboBoxDiaNac.setSelectedIndex(this.empleado.getFechaNacimiento().getDayOfMonth() -1);
    }
    
    private void cargarDepartamentosCombo(){
        jComboBoxDepartamentosEmpleado.removeAllItems();
        jComboBoxDepartamentosEmpleado.addItem("Sin Asignar");
        for (var dep : empleadoControlador.departamentoServicio.listarDepartamentos()) {
            jComboBoxDepartamentosEmpleado.addItem(dep.getNombre());
        }
    }
    
    
    private void actualizar(){
        var empleadoNew = new Empleado(this.empleado.getcodigoEmpleado(), 
                jTextFieldNombre.getText(),
                LocalDate.of(Integer.parseInt(jComboBoxAnioNac.
                        getSelectedItem().toString()), jComboBoxMesNac.
                getSelectedIndex()+1, jComboBoxDiaNac.getSelectedIndex()+1),
                jComboBoxNacionalidad.getSelectedItem().toString(),
                jTextFieldDireccion.getText(),jTextFieldCargo.getText(),
                Float.parseFloat(jTextFieldSalario.getText()),getDepartamentoFromComboBox());
        empleadoControlador.actualizarEmpleado(this.empleado.
                getcodigoEmpleado(), empleadoNew);
        
    }
    
    private Departamento getDepartamentoFromComboBox(){
        if(!jComboBoxDepartamentosEmpleado.getSelectedItem().toString().equalsIgnoreCase("Sin Asignar")){
            // Primer item del combo es "Sin Asignar" 
            Departamento dep = empleadoControlador.departamentoServicio.listarDepartamentos().get(jComboBoxDepartamentosEmpleado.getSelectedIndex() -1);
            return dep;
        }
        return null;
    }
    
